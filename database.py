def print_schedule_inline(schedule):
    for day, lessons in schedule.items():
        # Формируем строку уроков: "время - предмет"
        lessons_str = "; ".join([f"{time} - {subject}" for time, subject in lessons])
        print(f"{day}: {lessons_str}")

if __name__ == "__main__":
    print_schedule_inline(schedule)